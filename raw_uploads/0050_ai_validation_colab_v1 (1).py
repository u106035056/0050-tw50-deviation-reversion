    if pd.notna(rv80) and pd.notna(row.get("realized_vol_10bar")):
        if row.get("realized_vol_10bar") >= rv80:
            tags.append("High_Realized_Vol")

    if len(tags) == 0:
        tags.append("Normal_Loss_or_Unclassified")

    return "|".join(tags)

