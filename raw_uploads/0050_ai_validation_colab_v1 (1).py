def profit_factor(pnl):
    pnl = pnl.dropna()
    gross_profit = pnl[pnl > 0].sum()
    gross_loss = -pnl[pnl < 0].sum()
    if gross_loss == 0:
        return np.nan
    return gross_profit / gross_loss

def max_drawdown_from_pnl(pnl):
    pnl = pnl.fillna(0)
    cum = pnl.cumsum()
    peak = cum.cummax()
    dd = peak - cum
    return dd.max()

def summarize(df, group_cols):
    rows = []
